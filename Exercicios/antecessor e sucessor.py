#nesse Programa o usuario vai digitar um numero e o programa vai 
#mostra o antecessor e o sucessor
numero = input("Digite um numero:")

numero =  int(numero)

antecessor = numero -1
sucessor =  numero +1

print (f"O numero que você digitou e {numero}, o Antecessor dele é {antecessor} e o Sucessor é {sucessor}")




